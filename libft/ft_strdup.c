#include "libft.h"

char		*ft_strdup(const char *s)
{
	size_t	i;
	char	*dup;

	if (!(dup = (char *)malloc(sizeof(char) * ft_strlen(s) + 1)))
		return (0);
	i = 0;
	while (s[i])
	{
		dup[i] = s[i];
		i++;
	}
	dup[i] = '\0';
	return (dup);
}
