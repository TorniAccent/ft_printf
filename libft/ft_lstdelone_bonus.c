#include "libft.h"

void	ft_lstdelone(t_list *lst, void (*del)(void *))
{
	t_list	*tmp;

	if (!(lst && del))
		return ;
	tmp = lst;
	del(tmp->content);
	lst = lst->next;
	free(tmp);
}
